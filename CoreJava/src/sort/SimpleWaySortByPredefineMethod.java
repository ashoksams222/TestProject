package sort;

import java.util.Arrays;

public class SimpleWaySortByPredefineMethod {
	public static void main(String[] args) {
		int a[]= {4,2,6,1,5};
		System.out.println("Before Sort  "+Arrays.toString(a));
		Arrays.sort(a);
		System.out.println("After Sort  "+Arrays.toString(a));
	}

}
